    public void populateOrders()
    {




    HotelRep hotelRep = HotelRepDirectory.currentHotelRep; 
    HotelOrders hotelOrders = system.getHotelOrders(); 

    Vector<String> columnNames = new Vector<>();
    columnNames.add("Customer");
    columnNames.add("Order Value");

    Vector<Vector<Object>> rowData = new Vector<>();

    for (HotelOrder order : hotelOrders.getOrderList()) {
        if(order.getRestaurantId() == hotelRep.getHotelId()) { // Filter by hotelId
            Vector<Object> row = new Vector<>();
            row.add(system.getCustomerDirectory().getCustomerById(order.getCustomerId()).getName()); 
            row.add(order.getOrderTotal()); 

            // Add row to rowData
            rowData.add(row);
        }
    }

    // Create a table model
    DefaultTableModel model = new DefaultTableModel(rowData, columnNames);

    // Set the model to the table
    jTable1.setModel(model);

    // Assuming you have labels for total orders and total revenue


    // Calculate and display total orders and total revenue
    jTextField1.setText(String.valueOf(rowData.size()));
    double totalRevenue = rowData.stream()
                                 .mapToDouble(row -> (Float)row.get(1)) 
                                 .sum();
    jTextField2.setText(String.valueOf(totalRevenue));

    }
    
    
