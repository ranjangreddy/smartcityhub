        if(selectedRow < 0)
            JOptionPane.showMessageDialog(this, "Please select a row to proceed!");
        else
        {
            String hotelName = jTable1.getValueAt(selectedRow, 0).toString();
            Hotel hotel = system.getHotelDirectory().getHotelByName(hotelName);
            SpecificRestaurantJPanel panel = new SpecificRestaurantJPanel(system, hotel);
            CustomerExploreFoodJpanel.jSplitPane1.setRightComponent(panel);
            
            
        }
