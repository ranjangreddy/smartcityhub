    public void populateRestaurantsTable(String city)
    {
        ArrayList<Hotel> hotelsForCty = system.getHotelDirectory().hotelsForACity(city);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        
        for(Hotel hotel : hotelsForCty)
        {
            Object[] row = new Object[3];
            row[0] = hotel.getName();
            row[1] = hotel.getLocation();
            row[2] = hotel.getCuisine();

            model.addRow(row);  
            
        }        
        
        
    }
    
